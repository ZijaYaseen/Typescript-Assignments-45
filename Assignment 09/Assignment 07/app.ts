// Addition + :
function add (no1 : number , no2 : number) {
    return (no1 + no2)
}
console.log(add(2,6));

//Subtraction - :
function substract (no1 : number , no2 : number) {
    return (no1 - no2)
}
console.log(substract(100,92));

//Multiplication * :
function multiply (no1 : number , no2 : number) {
    return (no1 * no2)
}
console.log(multiply(4,2));


//Division / :
function divide (no3 : number , no4 : number) {
    return (no3 / no4)
}
console.log(divide(80,10));


//Modulus % :
function modulus (no3 : number , no4 : number) {
    return (no3 % no4)
}
console.log(modulus(88,10));


//Power / Square ** :
function power /*square*/ (no3 : number , no4 : number) {
    return (no3 ** no4)
}
console.log(power(2,3));